package Tabuada;

public class Tabuada{
	public int x;
	public int y;
	public int mult;
	
	
	public Tabuada() {
		super();
	
	}
	
	public Tabuada(int x, int y, int mult) {
		super();
		this.x = x;
		this.y = y;
		this.mult = mult;
	}

	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getMult() {
		return mult;
	}
	public void setMult(int mult) {
		this.mult = mult;
	}
	
	
}